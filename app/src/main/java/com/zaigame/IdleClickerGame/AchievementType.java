package com.zaigame.IdleClickerGame;

public enum AchievementType {
    TOTAL_CLICKS,          // Based on total times the star has been clicked
    TOTAL_STARDUST,        // Based on total stardust ever earned
    UPGRADE_COUNT,         // Based on owning a certain number of a specific upgrade
    REACH_SPS,              // Based on reaching a certain Stardust-per-second
    SINGULARITY_COUNT,
    RESEARCH_COUNT,
    ESSENCE_COUNT,
    COMBO_REACHED,
    CONSTELLATION_UNLOCKED,
    CHALLENGE_COMPLETED,
    AUTO_CLICKER_UNLOCKED,
    EVENTS_EXPERIENCED,
    RIFTS_COLLECTED
}